public class alternatecCharacter {

	public static void main(String[] args) {
		{
			 char ch1='A',ch2='Z';
			 while(ch1<='Z')
			 {
				 System.out.print(ch1);
				 ch1++;
				 }
				 System.out.println(" ");
				 while(ch2>='A')
				 {
				 System.out.print(ch2);
				 ch2--;
				 }
				 System.out.println(" ");
				 char ch3='A';
				 while(ch3<='Z')
				 {
				System.out.print(ch3);
				ch3+=2;//ch3=ch3+2
				 }
				 }
	}

}
